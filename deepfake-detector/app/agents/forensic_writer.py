# ─── Section generators ───────────────────────────────────────────────────────

def _get_score_bucket(score: float) -> str:
    if score >= 0.6:
        return "high_fake"
    elif score <= 0.4:
        return "low_fake"
    else:
        return "neutral"


def _get_temporal_bucket(ran: bool, score: float) -> str:
    if not ran:
        return "skipped"
    return "ran_high_fake" if score >= 0.55 else "ran_low_fake"


