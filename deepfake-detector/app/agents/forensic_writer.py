def _build_signal_findings(
    agent_results: list,
    fusion_weights: dict,
    rng: random.Random,
) -> str:
    lines = ["## FORENSIC SIGNAL FINDINGS\n"]

    signal_interp = {
        "pixel_artifact_score": (_SPATIAL_INTERPRETATIONS, "### 🖼️ Signal 1: Spatial Forensics (Pixel-Level Analysis)"),
        "spectral_artifact_score": (_FREQUENCY_INTERPRETATIONS, "### 📡 Signal 2: Frequency-Domain Analysis"),
        "temporal_inconsistency_score": (None, "### 🎬 Signal 3: Temporal Consistency (Video)"),
        "rppg_anomaly_score": (None, "### 💓 Signal 4: Biological Signal — rPPG"),
        "lip_sync_mismatch_score": (None, "### 🎙️ Signal 5: Audio-Visual Synchronisation"),
    }

    for ar in agent_results:
        signal = ar.get("signal_name", "")
        header = signal_interp.get(signal, (None, f"### {ar.get('agent_name', 'Agent')}"))[1]
        lines.append(header)
        lines.append("")

        score = ar.get("score", 0.5)
        conf = ar.get("confidence", 0.0)
        ran = ar.get("ran", True)
        weight = fusion_weights.get(signal, 0.0)
        direction = "FAKE-leaning" if score > 0.55 else ("REAL-leaning" if score < 0.45 else "Neutral")

        lines.append(f"**Score:** {score:.3f} | **Confidence:** {conf:.0%} | **Fusion Weight:** {weight:.1%} | **Signal Direction:** {direction}")
        lines.append("")

        if not ran:
            skipped_reason = ar.get("skipped_reason", "Not applicable.")
            if signal == "temporal_inconsistency_score":
                lines.append(rng.choice(_TEMPORAL_INTERPRETATIONS["skipped"]))
            elif signal == "rppg_anomaly_score":
                lines.append(rng.choice(_BIOLOGICAL_INTERPRETATIONS["skipped"]))
            elif signal == "lip_sync_mismatch_score":
                lines.append(rng.choice(_LIP_SYNC_INTERPRETATIONS["skipped"]))
            else:
                lines.append(f"*Skipped: {skipped_reason}*")
            lines.append("")
            continue

        details = ar.get("details", {})
        detail_note = details.get("note", "facial and background regions")

        if signal == "pixel_artifact_score":
            bucket = _get_score_bucket(score)
            templates = _SPATIAL_INTERPRETATIONS.get(bucket, _SPATIAL_INTERPRETATIONS["neutral"])
            txt = rng.choice(templates)
            lines.append(txt.replace("{detail_note}", str(detail_note)))
        elif signal == "spectral_artifact_score":
            bucket = _get_score_bucket(score)
            templates = _FREQUENCY_INTERPRETATIONS.get(bucket, _FREQUENCY_INTERPRETATIONS["neutral"])
            lines.append(rng.choice(templates))
        elif signal == "temporal_inconsistency_score":
            bucket = _get_temporal_bucket(ran, score)
            lines.append(rng.choice(_TEMPORAL_INTERPRETATIONS[bucket]))
        elif signal == "rppg_anomaly_score":
            bucket = _get_temporal_bucket(ran, score)
            lines.append(rng.choice(_BIOLOGICAL_INTERPRETATIONS[bucket]))
        elif signal == "lip_sync_mismatch_score":
            bucket = _get_temporal_bucket(ran, score)
            lines.append(rng.choice(_LIP_SYNC_INTERPRETATIONS[bucket]))
        else:
            lines.append(f"Score: {score:.3f}. No specialised interpretation available for this signal.")

        # Additional detail fields
        if details:
            notable = {k: v for k, v in details.items() if k not in ("note", "caveat", "error")}
            if notable:
                lines.append("")
                lines.append("*Technical measurements:*")
                for k, v in list(notable.items())[:5]:
                    lines.append(f"  - **{k.replace('_', ' ').title()}:** {v}")
            if "caveat" in details:
                lines.append("")
                lines.append(f"> ⚠️ **Caveat:** {details['caveat']}")

        lines.append("")

    return "\n".join(lines)


def _build_statistical_analysis(
    prob: float,
    conf: float,
    fusion_weights: dict,
    agent_results: list,
) -> str:
    active = [ar for ar in agent_results if ar.get("ran", True)]
    mean_agent_conf = sum(ar.get("confidence", 0) for ar in active) / max(len(active), 1)

    weight_table = "\n".join(
        f"| {s.replace('_score','').replace('_',' ').title()} | {w:.4f} | {w:.1%} |"
        for s, w in fusion_weights.items()
    )

    return f"""\
## STATISTICAL ANALYSIS

### Ensemble Fusion Results

| Metric | Value |
|---|---|
| Composite Deepfake Probability | **{prob:.4f}** ({prob:.1%}) |
| Verdict Confidence | **{conf:.4f}** ({conf:.0%}) |
| Active Agent Count | {len(active)} |
| Mean Agent Confidence | {mean_agent_conf:.1%} |

### Signal Weight Distribution

| Signal | Weight (raw) | Weight (%) |
|---|---|---|
{weight_table}

### Confidence Interpretation

{"⚠️ **Low confidence** — the system is uncertain and a human expert review is warranted." if conf < 0.40 else
 "✅ **Moderate confidence** — the verdict is grounded in consistent multi-signal evidence, though independent verification is advised." if conf < 0.70 else
 "🔵 **High confidence** — multiple independent signals strongly converge. This is a robust forensic finding."}

**Probability Interpretation:**
- Score > 0.75: High likelihood of synthetic origin
- Score 0.55–0.75: Probable synthetic content (LIKELY FAKE)
- Score 0.45–0.55: Inconclusive zone
- Score 0.25–0.45: Probable authentic content (LIKELY REAL)
- Score < 0.25: High likelihood of authentic origin

This sample scored **{prob:.3f}**, placing it in the **{
    'high-synthetic' if prob > 0.75 else
    'probable-synthetic' if prob > 0.55 else
    'inconclusive' if prob > 0.45 else
    'probable-authentic' if prob > 0.25 else
    'high-authentic'
} zone**.
"""


def _build_expert_opinion(
    verdict: str,
    prob: float,
    conf: float,
    media_type: str,
    rng: random.Random,
) -> str:
    if verdict == "LIKELY FAKE":
        stance = (
            f"It is this examiner's expert opinion that the submitted {media_type} exhibits "
            f"a pattern of forensic indicators that, taken together, provide a compelling case "
            f"for synthetic origin. The convergence of spatial, spectral{', temporal, and biological' if media_type == 'video' else ' '}"
            f"signals at a composite probability of {prob:.1%} — above the established threshold "
            f"for the LIKELY FAKE determination — supports this conclusion.\n\n"
            f"This finding should be considered in the context of the stated limitations below. "
            f"Independent corroboration by a human forensic examiner and, where possible, "
            f"access to the original, uncompressed source file is recommended before any "
            f"consequential determination is made."
        )
    elif verdict == "LIKELY REAL":
        stance = (
            f"It is this examiner's expert opinion that the submitted {media_type} does not "
            f"exhibit the forensic signatures characteristic of known synthetic generation methods "
            f"at the current detection sensitivity. The composite probability of {prob:.1%} places "
            f"the sample in the LIKELY REAL category with {conf:.0%} verdict confidence.\n\n"
            f"**This is not a certificate of authenticity.** Sophisticated adversarially-crafted "
            f"content, methods not represented in the training distribution, or generation techniques "
            f"released after this system's knowledge cutoff could evade detection. This finding "
            f"indicates absence of detected synthetic indicators — not confirmed authenticity."
        )
    else:
        stance = (
            f"The forensic evidence is inconclusive. Individual signals disagree and the composite "
            f"probability ({prob:.1%}) falls within the uncertainty band where neither synthetic nor "
            f"authentic determinations can be made with acceptable confidence ({conf:.0%}).\n\n"
            f"This examiner recommends: (1) acquisition of a higher-resolution or uncompressed "
            f"version of the sample; (2) manual review by a human expert; (3) supplementary analysis "
            f"using additional tools or methods not available in this pipeline. No action should be "
            f"taken based solely on this inconclusive finding."
        )

    return f"""\
## EXPERT OPINION

{stance}
"""


def _build_limitations() -> str:
    return """\
## LIMITATIONS AND CAVEATS

1. **Training distribution overfitting**: The spatial classifier was trained on specific deepfake datasets. 
   Generators not represented in the training data may produce outputs that evade detection.

2. **Compression sensitivity**: JPEG compression below quality 70 and H.264 encoding at high compression 
   ratios degrade frequency and spatial signals. Accuracy may be reduced for compressed media.

3. **Adversarial robustness**: A sophisticated actor who knows this system's detection signals 
   could craft inputs designed to evade them. This tool should not be the sole line of defence.

4. **No face detection guarantee**: If no face is detected, full-image analysis is performed — 
   which is less accurate than face-region-focused analysis.

5. **Quantum module**: The quantum-inspired weight optimisation runs as a classical simulation 
   (PennyLane, default.qubit backend). No quantum hardware is used and no quantum speedup is claimed.

6. **Diffusion model coverage**: Frequency analysis has been updated to target diffusion-model 
   artifacts (Corvi et al., 2023), but this remains an area of active research. Coverage of the 
   newest generation models (DALL-E 3, FLUX, SD3) is not fully validated.

7. **Legal admissibility**: This report is generated by an automated system and **is not intended 
   as legally admissible forensic evidence** without review by a certified human forensic examiner.
"""


def _build_recommendations(verdict: str) -> str:
    if verdict == "LIKELY FAKE":
        recs = [
            "Obtain the original, uncompressed source file for re-examination if possible.",
            "Cross-reference with blockchain/C2PA content provenance metadata if available.",
            "Submit for independent examination by a certified human digital forensics examiner.",
            "Document the full chain of custody for the media artifact.",
            "Do not publicly attribute or act on this finding without independent corroboration.",
        ]
    elif verdict == "LIKELY REAL":
        recs = [
            "Retain this report alongside the original media file for the record.",
            "Verify file metadata (EXIF, creation timestamps) for consistency.",
            "Note that this analysis cannot rule out sophisticated adversarial evasion.",
            "If consequential decisions depend on this determination, seek independent corroboration.",
        ]
    else:
        recs = [
            "Obtain a higher-resolution or uncompressed version of the sample for re-analysis.",
            "Commission independent expert review — this automated system cannot issue a reliable verdict.",
            "Do not make consequential decisions based on an inconclusive automated finding.",
            "Consider submitting the sample to additional detection pipelines for comparison.",
        ]

    recs_text = "\n".join(f"{i+1}. {r}" for i, r in enumerate(recs))
    return f"""\
## RECOMMENDATIONS

{recs_text}
"""


