# ─── Main entry point ─────────────────────────────────────────────────────────

def generate_forensic_report(
    verdict_dict: dict,
    filename: str = "unknown",
    input_hash: str = "",
) -> str:
    """
    Generate a full forensic specialist report from a verdict dict.

    Args:
        verdict_dict: The ForensicVerdict as a dict (from .model_dump())
        filename: Original filename of the analysed media
        input_hash: Hash of the input file for case ID generation

    Returns:
        Markdown-formatted forensic report string.
    """
    rng = random.Random(input_hash or filename)  # seeded for reproducibility per case

    verdict = verdict_dict.get("verdict", "UNCERTAIN")
    prob = verdict_dict.get("deepfake_probability", 0.5)
    conf = verdict_dict.get("confidence_in_verdict", 0.0)
    media_type = verdict_dict.get("media_type", "image")
    agent_results = verdict_dict.get("agent_results", [])
    fusion_weights = verdict_dict.get("fusion_weights", {})

    case_id = _make_case_id(input_hash)

    sections = [
        f"# 🔍 DIGITAL FORENSIC EXAMINATION REPORT\n",
        f"---\n",
        _build_executive_summary(verdict, prob, conf, media_type, case_id, filename, rng),
        _build_methodology(),
        _build_signal_findings(agent_results, fusion_weights, rng),
        _build_statistical_analysis(prob, conf, fusion_weights, agent_results),
        _build_expert_opinion(verdict, prob, conf, media_type, rng),
        _build_limitations(),
        _build_recommendations(verdict),
        _build_signature(case_id),
    ]

    return "\n\n".join(sections)
